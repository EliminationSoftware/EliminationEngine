namespace EliminationEngine.Tools
{
    public class Color
    {
        public byte R = 255;
        public byte G = 255;
        public byte B = 255;
        public byte A = 255;

        public Color(byte r, byte g, byte b, byte a)
        {
            R = r;
            G = g;
            B = b;
            A = a;
        }
    }
}
